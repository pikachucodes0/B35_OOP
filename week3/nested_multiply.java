public class nested_multiply{
    public static void main(String[] args) {
        int n=2;
        for(int i=10;i>1;i--){
            for(int j=1;j<11;j++){
                System.out.print(i+"*"+j+"="+(i*j)+"\t");
            }
            System.out.println("\n");
        }
    }
    
}
